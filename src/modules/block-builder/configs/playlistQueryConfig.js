import { useStaticQuery, graphql } from 'gatsby'

export const usePlaylistQuery = () => {
	const query = useStaticQuery(
		graphql`
			query {
				allFile(filter: { internal: { mediaType: { eq: "audio/mpeg" } } }) {
					edges {
						node {
							publicURL
						}
					}
				}
			}
		`
	)
	return query
}
